// 현재 테마 가져오기
export const getCurrentTheme = () => {
  const saved = localStorage.getItem('selectedTheme');
  return saved && themes[saved] ? saved : 'claude';
};

// 테마 변경
export const setTheme = (themeKey) => {
  if (themes[themeKey]) {
    localStorage.setItem('selectedTheme', themeKey);
    updateCSSVariables(themes[themeKey]);
  }
};

// CSS 변수 업데이트
export const updateCSSVariables = (theme) => {
  const root = document.documentElement;
  
  // Primary colors
  Object.entries(theme.primary).forEach(([key, value]) => {
    root.style.setProperty(`--color-primary-${key}`, value);
  });
  
  // Secondary colors  
  Object.entries(theme.secondary).forEach(([key, value]) => {
    root.style.setProperty(`--color-secondary-${key}`, value);
  });
};

// 초기화
export const initTheme = () => {
  const currentTheme = getCurrentTheme();
  updateCSSVariables(themes[currentTheme]);
};

export { themes };
export default themes;